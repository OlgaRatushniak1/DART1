package data;

public class Employee {
    private long id;
    private String name;
    private int birthYear;
    private int age;
    private double grossSalary;

    public Employee(){
        this.id=id;
        this.name=name;
        this.birthYear=birthYear;
        this.age=age;
        this.grossSalary=grossSalary;
    }

}

